#include <stdio.h>

int main() {
    int num1, num2, num3, num4, mayor;

    // Solicitar al usuario que ingrese los cuatro números
    printf("Ingrese el primer número: ");
    scanf("%d", &num1);
    printf("Ingrese el segundo número: ");
    scanf("%d", &num2);
    printf("Ingrese el tercer número: ");
    scanf("%d", &num3);
    printf("Ingrese el cuarto número: ");
    scanf("%d", &num4);

    // Determinar el mayor de los cuatro números
    mayor = num1; // Suponemos que el primer número es el mayor inicialmente

    if (num2 > mayor) {
        mayor = num2;
    }
    if (num3 > mayor) {
        mayor = num3;
    }
    if (num4 > mayor) {
        mayor = num4;
    }

    // Mostrar el resultado
    printf("El mayor de los cuatro números es: %d\n", mayor);

    return 0;
}
